        <!-- Instruction Text -->
        <p>Follow these steps to perform CPR:</p>
        <ul>
            <li>Check if the person is breathing. If not, proceed with CPR.</li>
            <li>Place the person on their back on a firm surface, like the ground or a table.</li>
            <li>Place your hands one on top of the other in the center of their chest, just below the breastbone.</li>
            <li>Begin chest compressions by pushing down hard and fast (about 2 inches deep and at a rate of 100-120 compressions per minute).</li>
            <li>After every 30 compressions, give 2 rescue breaths (if trained). To do this, pinch the person's nose, cover their mouth with yours, and give two slow breaths.</li>
            <li>Continue the cycle of 30 compressions and 2 breaths until help arrives or the person begins to breathe on their own.</li>
            <li>If the person starts breathing again, place them in the recovery position and monitor their breathing until medical help arrives.</li>
        </ul>
        
