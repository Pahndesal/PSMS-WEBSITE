        <p>Follow these steps to treat heat stroke:</p>
        <ul>
            <li>Immediately call for emergency medical help. Heat stroke is a medical emergency.</li>
            <li>Move the person to a cooler environment, ideally a shaded area or air-conditioned room.</li>
            <li>Remove any excess clothing to allow heat to escape from the body.</li>
            <li>Cool the person down by applying cold packs to their neck, armpits, or groin areas. If no cold packs are available, use cool, wet cloths.</li>
            <li>Encourage the person to drink cool fluids (water, not alcohol or caffeine). If the person is unconscious, do not give them fluids.</li>
            <li>If the person is conscious and able to swallow, continue cooling them with water and ice packs until help arrives.</li>
            <li>Monitor their breathing, pulse, and level of consciousness while waiting for medical assistance.</li>
        </ul>
        
