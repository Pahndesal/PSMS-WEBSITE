        <p>Follow these steps if someone is choking:</p>
        <ul>
            <li>I Assess ang sitwasyon at tanungin ang tao kung sila ay nalunok.</li>
            <li>Kung hindi sila makapagsalita, makatawa, o makahinga, malamang na sila ay nalunok at nangangailangan ng tulong.</li>
            <li>Hikayatin silang umubo kung kaya nila. Maaaring matulungan nito na mailabas ang bagay na humarang.</li>
            <li>Kung hindi sila makahinga o makatawa, magsagawa ng Heimlich maneuver:</li>
            <ul>
                <li>Tumayo sa likod ng tao at ipalupot ang iyong mga braso sa kanilang bewang.</li>
                <li>Gawin ang kamao ng isang kamay at ilagay ito sa itaas ng kanilang pusod, sa ilalim ng mga tadyang.</li>
                <li>Hawakan ang kamao gamit ang iyong kabilang kamay at itulak ito paitaas at papasok, matigas at mabilis.</li>
                <li>Ulitin ang mga pag-tulak hanggang sa mailabas ang bagay o dumating ang tulong..</li>
            </ul>
            <li>Kung mawalan ng malay ang tao, magsimula ng CPR at agad na tumawag ng emergency services.</li>
        </ul>
        
